    public void ContactHandler() {        
         // Añadir un Contacto---------------------------------------------------------------------
        AddBtn.addActionListener((ActionEvent e) -> {
            Random r = new Random();
            int low = 1722;
            int high = 6076;
            int id = r.nextInt(high-low) + low;
            IDField.setText(Integer.toString(id));
            Ops.Launcher(NewContact);
        });
        
        
        // Guardar Contacto------------------------------------------------------------------------
        SaveContact.addActionListener((ActionEvent e) -> {
            try {
                Ops.AddContact(NewContact, nonePic, ContactsContainer, filePath, IDField, NameField, LNameField, CorpField, PosField, NumField, EmailField, NotesField);
                nonePic.setIcon(null);
                filePath.setText(null);
                Random r = new Random();
                int low = 1722;
                int high = 6076;
                int id = r.nextInt(high-low) + low;
                IDField.setText(Integer.toString(id));
            } catch (IOException ex) {
                Logger.getLogger(Core.class.getName()).log(Level.SEVERE, null, ex);
            }
        });
        
        
        // Cancelar Registro-------------------------------------------------------------------------
        CancelBtn.addActionListener((ActionEvent e) -> {
            JOptionPane.showMessageDialog(NewContact, "El registro fue cancelado."); 
            Ops.CleanFields(NameField, LNameField, CorpField, PosField, NumField, EmailField, NotesField);
            NewContact.dispose();
        });
        
        OpenPanel.addActionListener((ActionEvent e) -> {
            Ops.MultiPanel(MultiPanel, nonePic1, SearchCon, EditCon, UpdateCon, DelCon, SearchField, IDInfo, InfoName, InfoLast, InfoCompany, InfoPos, InfoTel, InfoEmail, InfoNotes);
        });
        
        PanelDone.addActionListener((ActionEvent e) -> {
            SearchField.setText(null);
            Ops.CleanFields(InfoName, InfoLast, InfoCompany, InfoPos, InfoTel, InfoEmail, InfoNotes);
            nonePic1.setIcon(null);
            IDInfo.setText(null);
            MultiPanel.dispose();
        });
        
        
    }
    
