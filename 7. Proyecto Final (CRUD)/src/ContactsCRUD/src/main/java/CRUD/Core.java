    public void ImageHandler() {
        try {
                JFileChooser chooser = new JFileChooser();
                FileNameExtensionFilter filter = new FileNameExtensionFilter("png", "jpg");
                chooser.setFileFilter(filter);
                int approve = chooser.showOpenDialog(null);
                if(approve == JFileChooser.APPROVE_OPTION){
                   File f = chooser.getSelectedFile();
                   String path = f.getAbsolutePath();
                   filePath.setText(path);
                   Image foto = getToolkit().getImage(path);
                   foto = foto.getScaledInstance(110, 110,Image.SCALE_DEFAULT);
                   nonePic.setIcon(new ImageIcon(foto));
                }
                } catch(HeadlessException Ex) {JOptionPane.showMessageDialog(null, Ex);}
    }
